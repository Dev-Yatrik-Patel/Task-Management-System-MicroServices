from typing import Any
from fastapi.responses import JSONResponse

def success_response(
    *,
    message: str = "Success",
    data: Any = None,
    status_code: int = 200
):
    return JSONResponse(
        status_code=status_code,
        content={
            "success": True,
            "message": message,
            "data": data,
            "error": None
        }
    )


def error_response(
    *,
    message: str,
    error_code: str = "ERROR",
    status_code: int,
    details: str | None = None
):
    return JSONResponse(
        status_code=status_code,
        content={
            "success": False,
            "message": message,
            "data": None,
            "error": {
                "code": error_code,
                "details": details
            }
        }
    )